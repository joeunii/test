import math
print(math.pi)
print(math.e)

#절대값
print(abs(-10))

#반올림
print(round(1.5))

#버림
print(math.trunc(1.9))

#팩토리얼
print(math.factorial(5))

#제곱 연산
print(3**3)

#**와 같은 기능
print(math.pow(3,3))

#제곱근
print(math.sqrt(4))

#로그
print(math.log(4,2)) #log2의 4를 계산
print(math.log10(1000)) #밑수가 10인 로그를 계산